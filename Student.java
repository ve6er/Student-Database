class Student{
	private String name;
	private int prn;
	private String dob;

	public void setName(String name){
		this.name= name;
	}
	
	public String getName(){
		return name;
	}

	public void setPrn(int prn){
		this.prn=prn;
	}
	
	public int getPrn(){
		return prn;
	}

	public void setDob(String dob){
		this.dob= dob;
	}
	
	public String getDob(){
		return dob;
	}
}